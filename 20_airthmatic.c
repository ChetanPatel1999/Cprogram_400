// airthmatic operator example
#include <stdio.h>
void main()
{
    int a, b, ans;
    a = 23;
    b = 5;
    printf("value of a : %d\n", a);
    printf("value of b : %d\n", b);
    ans = a + b;
    printf("addition : %d\n", ans);
    ans = a - b;
    printf("subtraction : %d\n", ans);
    ans = a * b;
    printf("multiplication : %d\n", ans);
    ans = a / b;
    printf("division : %d\n", ans);
    ans = a % b;
    printf("reminder : %d \n", ans);
}